package cz.cas.mbu.genexpi.compute;

public class GNException extends RuntimeException {

	public GNException() {
		super();
	}

	public GNException(String message, Throwable cause) {
		super(message, cause);
	}

	public GNException(String message) {
		super(message);
	}

	public GNException(Throwable cause) {
		super(cause);
	}

}
